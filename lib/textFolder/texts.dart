 Kōtakkal Āmina was a member of the famous Maqdoom family of Ponnani, known for their extreme religious.
 Musliyar's grandfather, Mūsa, was one of several "Malappuram Martyrs". Ali Musliyar began his education studying the Qur'an
, tajwīd and the Malayālam language with Kakkadammal Kunnukammu Molla. He was sent to Ponnani Darse for further studies in religion and philosophy,
 under the tutelage of Sheikh Zainuddin Maqdum I (Akhir), which he successfully completed after 10 years.[citation needed]

He then went to Haram, Mecca for further education. Throughout this period, he was guided by several famous scholars,
